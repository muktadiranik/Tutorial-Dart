void main() {
  // String
  String a = "string";
  print("String: $a");

  // int
  int b = 10;
  print("int: $b");

  // double
  double c = 10.22;
  print("double: $c");

  // bool
  bool d = true;
  print("bool: $d");

  // dynamic
  dynamic e = {
    'name': 'John',
    'age': 34,
  };
  print("dynamic: $e");
}
