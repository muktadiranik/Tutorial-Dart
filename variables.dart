void main() {
  // variables
  int x = 10;
  print(x);

  // var
  var y = {
    'name': 'John',
    'age': 36,
  };
  print(y);

  String a = "string";
  print(a);

  dynamic b = 20;
  print(b);

  final String d = "final";
  print(d);

  const Object c = {
    'name': 'John',
    'age': 36,
  };
  print(c);
}
